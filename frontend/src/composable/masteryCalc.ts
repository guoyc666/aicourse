// 掌握度计算包括答题表现分、学习行为分、复习巩固分
// M = w1 * A + w2 * S + w3 * T
// w1, w2, w3: 权重
// A: 答题表现分，题目有0-1的难度系数，题目类型为choice、fill或code
//    A = (加权正确率) * (加权答题数量) * (平均答题速度)
//    加权正确率 = Σ(题目难度系数 * 是否正确) / Σ(题目难度系数)
//    加权答题数量
//    平均答题速度设置：若学生平均答题时间小于等于标准时间，则为1分；
//                    若学生平均答题时间大于标准时间，则分数线性递减，最低为0.5分
// S: 学习行为分
//    S = 学习进度分 * 学习时长分
//    学习进度分 = 学习进度比例 * 1分
//    学习时长分设置：若学生学习时长大于等于标准时长，则为1分；
//                  若学生学习时长小于标准时长，则分数线性递减，最低为0分
// T: 复习巩固分
//    T根据遗忘曲线模型计算，考虑最后复习时间和复习次数
//    T = e^(-λ * Δt) * (1 - e^(-μ * N))
//    其中，Δt为距离最后一次复习的时间（天数），N为复习次数，λ和μ为调节参数
// 其中，当A>=0.8时，M不会低于0.7；当A<0.5时，M不会高于0.6，以确保答题表现对掌握度有较大影响。
// 其余时刻权重初步设置为w1=0.5, w2=0.3, w3=0.2，可以根据学生最终成绩与掌握度的相关性，动态调整权重。